#import "publiCattribute.h"

static publiCattribute *_sharedModel = nil;

@implementation publiCattribute

+ (publiCattribute *)sharedModel {
    
    if (_sharedModel == nil) {
        _sharedModel = [[self alloc] init];
    }
    return _sharedModel;
}

@end
